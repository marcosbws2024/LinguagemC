#include <stdio.h>

int main()
{
    /*Incremento(++)
     Pré-incremento ++a
     Pós-incremento a++

     Decremento(--) 
     Pré-decremento --a
     Pós-decremento a--
    */
    int a = 10;
    printf("%d\n", a);
     a++;
    printf("%d\n", a);
     a++;
    printf("%d\n", a);
     a++;
    printf("%d\n", a);


    a--;
    printf("%d\n", a);
    
    a--;
    printf("%d\n", a);
    
    a--;
    printf("%d\n", a);

   return 0;
}